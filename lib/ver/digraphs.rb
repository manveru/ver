# They most likely won't change that part much in future, but just in case we
# have to do something with their #ifdef statements, i keep the code around.

puts 'module VER'
puts '  DIGRAPHS = {'

File.open('/home/manveru/pkgbuilds/vim/src/vim72/src/digraph.c', 'rb') do |file|
  file.each_line do |line|
    if line =~ /^\s+\{'(.+)', '(.+)', (0x\h+)\},$/
      chr = [Integer($3)].pack('U')
      digraph = "#$1#$2"
      digraph.sub!(/\\'/, "'")
      puts "%8p => %11p," % [digraph, chr]
    end
  end
end

puts '  }'
puts 'end'
